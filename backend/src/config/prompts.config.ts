export const COMPARE_PROMPT = `请对比以下保险方案，从投保人角度分析关键差异。

## 方案列表
{plans}

## 对比维度与优先级

### 第一优先级（直接影响保障体验）
1. **保障额度**：一般医疗和重疾医疗的保额高低
2. **免赔额**：免赔额设置是否友好（越低越好）
3. **报销比例**：经社保后实际报销比例
4. **续保条件**：是否保证续保，续保是否需要审核

### 第二优先级（保障完整性）
5. **保障范围**：是否包含质子重离子、院外特药等
6. **病种覆盖**：重疾/轻症/中症种类数量
7. **赔付次数**：多次赔付的分组和间隔期要求

### 第三优先级（投保条件）
8. **等待期**：等待期长短（越短越好）
9. **投保年龄**：可投保年龄范围
10. **职业类别**：可投保职业范围

## 输出格式
JSON数组，每个元素表示一个差异点：

{
  "field": "字段英文名",
  "fieldLabel": "字段中文名",
  "values": [
    {"planId": "方案1id", "planName": "方案名称", "value": "该方案该字段的值"}
  ],
  "impact": "advantage/disadvantage/neutral/same",
  "summary": "对该差异的客观分析说明"
}

## 影响判断规则
- advantage: 该方案在此项优于其他方案，对投保人更有利
- disadvantage: 该方案在此项弱于其他方案
- neutral: 各方案此项差异对投保人影响不大
- same: 各方案此项基本相同

## 输出要求
1. 只输出JSON数组，不要任何解释
2. 只输出真正有差异的项目，相同的项目不输出
3. 按影响程度从大到小排序
4. 每个差异的summary要客观分析，不要有倾向性`;
